// ==================================================
// 乱数生成
// ==================================================
export function random(min, max) {
    return Math.floor(Math.random() * (max + 1 - min)) + min;
};
// ==================================================
// 線形補間
// ==================================================
export function lerp(x, y, a) {
    return (1 - a) * x + a * y;
};
// ==================================================
// 度数法 -> 弧度法
// ==================================================
export function toRad(deg) {
    return (deg * Math.PI) / 180;
};
// ==================================================
// 弧度法 -> 度数法
// ==================================================
export function toDeg(rad) {
    return rad * (180 / Math.PI);
};